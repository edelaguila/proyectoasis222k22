    public String buscarVideo(){
        JFileChooser archivo = new JFileChooser();
        int resultado = archivo.showOpenDialog(this);
        
        if(resultado != JFileChooser.CANCEL_OPTION){
            return archivo.getSelectedFile().getAbsolutePath().replace("\\", "/");
        }
        return null;
    }
    
    
