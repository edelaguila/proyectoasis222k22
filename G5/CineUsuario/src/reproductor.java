    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        try{
            if(this.repro.getMediaplayer() != null){
                this.repro.parar();
            }
            String ruta = buscarVideo();
            if(!ruta.isEmpty()){
                String nombre = ruta.split("/")[ruta.split("/").length-1];
                this.titulo.setText(nombre);
                
                this.repro.setRuta(ruta);
                this.repro.setJpanel(jPanel1);
                this.repro.mostrarvideo();
                this.repro.reproducir();
            }
        }catch(NullPointerException e){
            JOptionPane.showMessageDialog(null, "No selecciono su trailer");
        }
    }//GEN-LAST:event_jButton2ActionPerformed

