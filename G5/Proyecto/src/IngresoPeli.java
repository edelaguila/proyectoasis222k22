    private void GrabarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_GrabarActionPerformed
        conectar cc=new conectar();
        Connection cn=cc.conexion();
        try{
            PreparedStatement pst=cn.prepareStatement("INSERT INTO peliculas(id_peli,nombrePeli,clasificacion,director,fecha_estreno,duracion,foto) VALUES(?,?,?,?,?,?,?)");
            FileInputStream archivofoto;
            
            pst.setString(1,id.getText());
            pst.setString(2,nom.getText());
            pst.setString(3,cla.getText());
            pst.setString(4,direc.getText());
            pst.setString(5,fecha.getText());
            pst.setString(6,dura.getText());
            archivofoto= new FileInputStream(imagen.getText());
            pst.setBinaryStream(7,archivofoto); 

            int a=pst.executeUpdate();
            if(a>0){
                JOptionPane.showMessageDialog(null,"Registro exitoso");
                mostrardatos("");
                setVisible(false);
            }
            else{
                JOptionPane.showMessageDialog(null,"Error al agregar");
            }
        }catch(Exception e){
    }//GEN-LAST:event_GrabarActionPerformed
    }
    private void selecimageActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_selecimageActionPerformed
        JFileChooser archivo = new JFileChooser();
        archivo.setDialogTitle("Abrir Archivo");
        int ventana = archivo.showOpenDialog(null);
        if(ventana == JFileChooser.APPROVE_OPTION)
        {
            File file = archivo.getSelectedFile();
            imagen.setText(String.valueOf(file));
            Image foto= getToolkit().getImage(imagen.getText());
            foto = foto.getScaledInstance(110,110,Image.SCALE_DEFAULT);
            labelfoto.setIcon(new ImageIcon(foto));
                    
            }
    }//GEN-LAST:event_selecimageActionPerformed

